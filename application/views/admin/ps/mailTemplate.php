Nu är schemat för alla 120 Personliga Samtal lagt! Wohow.
Har du förhinder är det viktigt att du meddelar detta och inte bara lämnar företaget väntande på dig.
Det finns fortfarande möjlighet för spontanbokning av Personliga Samtal, om du tillsammans med ett företag vill boka en tid i ett rum under dagen. Först till kvarn gäller och bokningen sker genom företagsreceptionen mellan StiL och Ljusgården.

Samtalen äger rum i grupprum på campus: Grupprummen på hyllan C-huset, Wallenbergsrummen B-huset och grupprum Ettan till Sjuan i A-huset. 
Rummen är numrerade 1-20 med en lapp på dörren. Nytt samtal börjar varje hel och halvtimma från 09.00 till 15.00, samtalen är alltså mellan 20 till 30 minuter långa. 
Kom i tid och avsluta samtalen i tid! Se till att hitta dit du ska och använd receptionerna för att ställa dina frågor. 

